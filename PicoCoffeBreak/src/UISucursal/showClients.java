    private void obtainAllUsers() {

        Connection connection = null;
        Driver driver = new org.apache.derby.jdbc.ClientDriver();
        String URLDerby = "jdbc:derby://localhost:1527/Central";
        Statement statement = null;
        ResultSet resultSet = null;
        try {
            DriverManager.registerDriver(driver);
            connection = DriverManager.getConnection(URLDerby, "Central", "Central");
            String consulta = "SELECT * FROM Usuario";
            statement = connection.createStatement();
            resultSet = statement.executeQuery(consulta);
            while (resultSet.next()) {
                usuario = new Usuario();
                usuario.setCi(resultSet.getInt("Ci"));
                usuario.setNombre(resultSet.getString("Nombre"));
                usuario.setFechaNacimiento(resultSet.getString("Fecha_Nacimiento"));
                lista.add(usuario);
            }
            resultSet.close();
            resultSet = null;
            statement.close();
            statement = null;
            connection.close();
            connection = null;
        } catch (SQLException ex) {
        }
    }

    private void fillTable() {
        obtainAllUsers();
        model.setColumnIdentifiers(new String[]{"Nombre", "Ci", "Cumplea√±os"});
        for (Usuario usuarioActual : lista) {
            model.addRow(new String[]{usuarioActual.getNombre(), usuarioActual.getCi().toString(), usuarioActual.getFechaNacimiento()});
            System.out.println(usuarioActual.toString());
        }
    }

