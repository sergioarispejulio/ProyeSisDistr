    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int pos = jTable1.getSelectedRow();
        for (int i = 0; i < listaProductos.size(); i++) {
            if (listaProductos.get(i).getNro() == lista.get(pos).getNro()) {
                listaProductos.get(i).setCantidad(listaProductos.get(i).getCantidad() + lista.get(pos).getCantidad());
                total = total - (lista.get(pos).getCantidad() * lista.get(pos).getPrecio());
                break;
            }
        }
        lista.remove(pos);
        String titulos[] = {"Nombre", "Cantidad", "Precio"};
        String datos[][] = {};
        DefaultTableModel model = new DefaultTableModel(datos, titulos);
        for (int i = 0; i < lista.size(); i++) {
            Object data[] = {lista.get(i).getNombre(), lista.get(i).getCantidad(), lista.get(i).getCantidad() * lista.get(i).getPrecio()};
            model.addRow(data);
        }
        jTable1.setModel(model);
        jComboBox1.setSelectedIndex(0);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        nue = new Producto();
        nue.setNro(sele.getNro());
        nue.setNombre(sele.getNombre());
        nue.setPrecio(sele.getPrecio() * Integer.parseInt(jSpinner1.getValue().toString()));
        nue.setCantidad(Integer.parseInt(jSpinner1.getValue().toString()));
        lista.add(nue);
        String titulos[] = {"Nombre", "Cantidad", "Precio"};
        String datos[][] = {};
        DefaultTableModel algo = new DefaultTableModel(datos, titulos);
        for (int i = 0; i < lista.size(); i++) {
            Object data[] = {lista.get(i).getNombre(), lista.get(i).getCantidad(), lista.get(i).getCantidad() * lista.get(i).getPrecio()};
            algo.addRow(data);
        }
        jTable1.setModel(algo);
        total = total + (nue.getPrecio() * nue.getCantidad());
        for (int i = 0; i < listaProductos.size(); i++) {
            if (listaProductos.get(i).getNro() == nue.getNro()) {
                listaProductos.get(i).setCantidad(listaProductos.get(i).getCantidad() - nue.getCantidad());
                break;
            }
        }
        nue = null;

    }//GEN-LAST:event_jButton5ActionPerformed

